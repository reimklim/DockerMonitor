def get_icon_image(icon_name: str, size: int = 24, color: str = "#FFFFFF") -> Optional[ctk.CTkImage]:
    """
    Get an icon image for use in the UI.
    
    Args:
        icon_name: Name of the icon
        size: Size of the icon in pixels
        color: Color of the icon (hex string)
        
    Returns:
        CTkImage object or None if icon not found
    """
    # Импортируем здесь, чтобы избежать проблем с циклическими импортами
    from utils.globals import DISABLE_IMAGES
    
    # Если изображения отключены, возвращаем None
    if DISABLE_IMAGES:
        return None
        
    # Create a cache key
    cache_key = f"{icon_name}_{size}_{color}"
    
    # Check if icon is already cached
    if cache_key in _icon_cache:
        return _icon_cache[cache_key]
    
    # Путь к возможному файлу SVG
    svg_path = os.path.join(os.path.dirname(__file__), f"temp_{icon_name}.svg")
    
    # Проверяем наличие файла SVG
    if os.path.exists(svg_path):
        try:
            # Читаем данные из файла SVG
            with open(svg_path, 'r') as file:
                svg_data = file.read()
        except Exception as e:
            logger.warning(f"Failed to read SVG file: {e}")
            svg_data = ICONS.get(icon_name, "")
    else:
        # Check if icon exists in predefined icons
        if icon_name not in ICONS:
            logger.warning(f"Icon '{icon_name}' not found")
            return None
        svg_data = ICONS[icon_name]
    
    # Создаем заглушку для иконки
    try:
        # Process SVG data
        from PIL import Image, ImageDraw
        import io
        
        # Создаем пустое изображение
        img = Image.new('RGBA', (size, size), (0, 0, 0, 0))
        
        try:
            # Пробуем использовать cairosvg
            import cairosvg
            
            # Get SVG data and replace color
            svg_data = svg_data.replace('stroke="currentColor"', f'stroke="{color}"')
            
            # Convert SVG to PNG using cairosvg
            png_data = cairosvg.svg2png(bytestring=svg_data.encode('utf-8'), 
                                       output_width=size, 
                                       output_height=size)
            
            # Create PIL Image from PNG data
            img = Image.open(io.BytesIO(png_data))
        except Exception as e:
            # Если произошла ошибка с cairosvg, создаем простую заглушку
            logger.warning(f"Error rendering icon with cairosvg: {e}")
            draw = ImageDraw.Draw(img)
            
            # Parse the color
            if color.startswith('#'):
                # Convert hex color to RGB
                r = int(color[1:3], 16)
                g = int(color[3:5], 16)
                b = int(color[5:7], 16)
            else:
                # Default color (white)
                r, g, b = 255, 255, 255
            
            # Draw a simple shape
            padding = int(size * 0.2)
            draw.rectangle([padding, padding, size-padding, size-padding], 
                          outline=(r, g, b, 255), width=max(1, int(size * 0.05)))
        
        # Create CTkImage
        icon_image = ctk.CTkImage(
            light_image=img,
            dark_image=img,
            size=(size, size)
        )
        
        # Cache and return the image
        _icon_cache[cache_key] = icon_image
        return icon_image
        
    except Exception as e:
        logger.error(f"Error creating icon image '{icon_name}': {e}")
        
        # Создаем пустое изображение как крайний случай
        try:
            from PIL import Image
            img = Image.new('RGBA', (size, size), (0, 0, 0, 0))
            icon_image = ctk.CTkImage(
                light_image=img,
                dark_image=img,
                size=(size, size)
            )
            return icon_image
        except:
            return None

def get_icon_svg(icon_name: str, color: str = "currentColor") -> Optional[str]:
    """
    Get SVG data for an icon.
    
    Args:
        icon_name: Name of the icon
        color: Color of the icon
        
    Returns:
        SVG data as string or None if icon not found
    """
    # Check if icon exists
    if icon_name not in ICONS:
        logger.warning(f"Icon '{icon_name}' not found")
        return None
        
    # Get SVG data
    svg_data = ICONS[icon_name]
    
    # Replace color if needed
    if color != "currentColor":
        svg_data = svg_data.replace('stroke="currentColor"', f'stroke="{color}"')
        
    return svg_data

def clear_cache():
    """Clear the icon cache."""
    _icon_cache.clear()
